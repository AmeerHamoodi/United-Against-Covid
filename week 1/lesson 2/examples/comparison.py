x = 1
print(x == 1) # returns true if x is equal to 1
print(x != 1) # returns true if x does not equal 1
print(x > 1) # returns true if x is greater than 1
print(x < 1) # returns true if x is less than 1
print(x >= 1) # returns true if x is greater than or equal to 1
print(x <= 1) # returns true if x is less than or equal to 1
